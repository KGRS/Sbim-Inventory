    private void SearchSupplierByCode(String CategoryCode) {
        try {
            ResultSet reset;
            Statement stmt;
            String query;
            int rowCount = 0;
            RefreshTable();

            if (!CategoryCode.equals("")) {
                query = "SELECT * FROM Suppliers WHERE SupplierCode LIKE '" + CategoryCode + "%'";
            } else {
                query = "SELECT * FROM Suppliers  WHERE SupplierCode LIKE '" + CategoryCode + "%'";
            }
            stmt = ConnectSql.conn.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE, ResultSet.CONCUR_UPDATABLE);
            reset = stmt.executeQuery(query);

            while (reset.next()) {

                model_SupplierTable.addRow(new Object[model_SupplierTable.getColumnCount()]);
                TableSupplier.setValueAt(reset.getString("SupplierCode"), rowCount, 0);
                TableSupplier.setValueAt(reset.getString("SupplierName"), rowCount, 1);
                TableSupplier.setValueAt(reset.getString("Supplied"), rowCount, 2);
                rowCount++;
            }
            reset.close();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            JOptionPane.showMessageDialog(this, "Please contact for support.");
        }
    }

