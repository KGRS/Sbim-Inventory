    private void CheckBeforeDelete() {

        String Code = txtSupplierCode.getText();
        if (!Code.isEmpty()) {
            try {
                java.sql.Statement stmt = ConnectSql.conn.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE, ResultSet.CONCUR_UPDATABLE);
                String query = "select SupplierCode From Items where SupplierCode = '" + Code + "'";
                ResultSet rset = stmt.executeQuery(query);

                java.sql.Statement stmt1 = ConnectSql.conn.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE, ResultSet.CONCUR_UPDATABLE);
                String query1 = "select SupplierCode From GRNMain where SupplierCode = '" + Code + "'";
                ResultSet rset1 = stmt1.executeQuery(query1);

                java.sql.Statement stmt2 = ConnectSql.conn.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE, ResultSet.CONCUR_UPDATABLE);
                String query2 = "select SupplierCode From PurchaseOrderMain where SupplierCode = '" + Code + "'";
                ResultSet rset2 = stmt2.executeQuery(query2);

                if (rset.next() || rset1.next() || rset2.next()) {
                    JOptionPane.showMessageDialog(this, "This supplier is already used. Can't delete.", "Can't delete", JOptionPane.ERROR_MESSAGE);
                } else if (!rset.next()) {
                    Delete();
                }

            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
                JOptionPane.showMessageDialog(this, "Please contact for support.");
            } catch (HeadlessException ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
                JOptionPane.showMessageDialog(this, "Please contact for support.");
            }
        } else if (Code.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please insert a valid supplier code before delete.", "Empty supplier code", JOptionPane.OK_OPTION);
            txtSupplierCode.requestFocus();
        }
    }

    private void Delete() {
        String Code = txtSupplierCode.getText();
        int x = JOptionPane.showConfirmDialog(this, "Are you sure To delete this?", "Delete supplier?", JOptionPane.YES_NO_OPTION);
        if (x == JOptionPane.YES_OPTION) {
            try {
                java.sql.Statement stmt = ConnectSql.conn.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE, ResultSet.CONCUR_UPDATABLE);
                java.sql.Statement Checkstmt = ConnectSql.conn.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE, ResultSet.CONCUR_UPDATABLE);

                String Checkquery = "select SupplierCode From Suppliers where SupplierCode = '" + Code + "'";
                ResultSet Checkrset = Checkstmt.executeQuery(Checkquery);

                if (Checkrset.next()) {
                    String query = "delete From Suppliers where SupplierCode = '" + Code + "'";
                    stmt.execute(query);
                    JOptionPane.showMessageDialog(this, "Supplier is deleted.");
                    Refresh();
                } else {
                    JOptionPane.showMessageDialog(this, "Invalid supplier code. Please insert a valid supplier code.", "Invalid supplier code", JOptionPane.OK_OPTION);
                    txtSupplierCode.requestFocus();
                }

            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
                JOptionPane.showMessageDialog(this, "Please contact for support.");
            } catch (HeadlessException ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
                JOptionPane.showMessageDialog(this, "Please contact for support.");
            }

        } else if (x == JOptionPane.NO_OPTION) {
            txtSupplierCode.requestFocus();
        }
    }

