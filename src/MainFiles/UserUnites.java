    private void TableCategoryMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TableCategoryMouseClicked
        String Code, Name, purchaseMeasurement, issueMeasurement, BaseUnitCode = "", remarks = "", UnitType = "";

        Code = TableCategory.getValueAt(TableCategory.getSelectedRow(), 0).toString();
        Name = TableCategory.getValueAt(TableCategory.getSelectedRow(), 1).toString();
        purchaseMeasurement = TableCategory.getValueAt(TableCategory.getSelectedRow(), 2).toString();
        issueMeasurement = TableCategory.getValueAt(TableCategory.getSelectedRow(), 3).toString();

        try {
            ResultSet reset;
            Statement stmt;
            String query;
            query = "SELECT [UnitCode]\n"
                    + "      ,[UnitName]\n"
                    + "      ,[PurchaseMeasurement]\n"
                    + "      ,[IssueMeasurement]\n"
                    + "      ,[Remarks]\n"
                    + "      ,[BaseUnitCode]\n"
                    + "      ,[UnitType]\n"
                    + "  FROM [SIUnits] where UnitCode = '" + Code + "'";
            stmt = ConnectSql.conn.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE, ResultSet.CONCUR_UPDATABLE);
            reset = stmt.executeQuery(query);

            if (reset.next()) {
                remarks = reset.getString("Remarks");
                BaseUnitCode = reset.getString("BaseUnitCode");
                UnitType = reset.getString("UnitType");

            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            JOptionPane.showMessageDialog(this, "Please contact for support.");
        }

        txtCode.setText(Code);
        txtCategoryName.setText(Name);
        FormattedTextPurchaseMeasurement.setText(purchaseMeasurement);
        FormattedTextIssueMeasurement.setText(issueMeasurement);
        cmbBaseSiUnit.setSelectedItem(BaseUnitCode);
        cmbTypes.setSelectedItem(UnitType);
        textRemarks.setText(remarks);
        txtCategoryName.requestFocus();
    }//GEN-LAST:event_TableCategoryMouseClicked

