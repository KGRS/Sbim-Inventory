    private void LoadCategories() {
        try {
            ResultSet reset;
            Statement stmt;
            String query;
            int rowCount = 0;
            query = "SELECT * FROM SIUnits ORDER BY UnitName";
            stmt = ConnectSql.conn.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE, ResultSet.CONCUR_UPDATABLE);
            reset = stmt.executeQuery(query);

            while (reset.next()) {
                model_categoryTable.addRow(new Object[model_categoryTable.getColumnCount()]);
                TableCategory.setValueAt(reset.getString("UnitCode"), rowCount, 0);
                TableCategory.setValueAt(reset.getString("UnitName"), rowCount, 1);
                TableCategory.setValueAt(reset.getString("PurchaseMeasurement"), rowCount, 2);
                TableCategory.setValueAt(reset.getString("IssueMeasurement"), rowCount, 3);
                rowCount++;
            }
            reset.close();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            JOptionPane.showMessageDialog(this, "Please contact for support.");
        }
    }

    private void loadBaseSiUnitToCombo() {
        try {
            java.sql.Statement stmt = ConnectSql.conn.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE, ResultSet.CONCUR_UPDATABLE);
            String query = "select BaseUnitCode From SIUnitsBase order by BaseUnitCode";
            ResultSet rset = stmt.executeQuery(query);

            cmbBaseSiUnit.removeAllItems();
            cmbBaseSiUnit.insertItemAt("--Select--", 0);
            int position = 1;
            if (rset.next()) {
                do {
                    cmbBaseSiUnit.insertItemAt(rset.getString("BaseUnitCode"), position);
                    position++;
                } while (rset.next());
            }
            cmbBaseSiUnit.setSelectedIndex(0);

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Error", ERROR);
        }
    }

