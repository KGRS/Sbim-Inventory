    private void Refresh() {
        RefreshTableAndLoadAgain();
        txtCode.setText("");
        txtCategoryName.setText("");
        cmbTypes.setSelectedIndex(0);
        txtSearch.setText("");
        FormattedTextPurchaseMeasurement.setText("1.0");
        FormattedTextIssueMeasurement.setText("1.0");
        textRemarks.setText("");
    }

    private void RefreshTableAndLoadAgain() {
        try {
            int row = model_categoryTable.getRowCount();
            for (int j = 0; j < row; j++) {
                model_categoryTable.removeRow(0);
            }
            LoadCategories();
            loadBaseSiUnitToCombo();
            loadTypes();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            JOptionPane.showMessageDialog(this, "Please contact for support.");
        }
    }

