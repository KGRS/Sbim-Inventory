    private void CheckBeforeSave() {
        String CategoryCode = txtCode.getText();
        String CategoryName = txtCategoryName.getText();
        String BaseUnitID = cmbBaseSiUnit.getSelectedItem().toString();
        String Type = cmbTypes.getSelectedItem().toString();
        String remarks = textRemarks.getText();
        String purchaseMeasurement = FormattedTextPurchaseMeasurement.getText();
        String issueMeasurement = FormattedTextIssueMeasurement.getText();
        if (!CategoryCode.isEmpty() && !CategoryName.isEmpty() && !Type.equals(select)
                && !BaseUnitID.equals(select) && !purchaseMeasurement.isEmpty() && !issueMeasurement.isEmpty()) {
            try {
                java.sql.Statement stmt = ConnectSql.conn.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE, ResultSet.CONCUR_UPDATABLE);
                String query = "select UnitCode From SIUnits where UnitCode = '" + CategoryCode + "'";
                ResultSet rset = stmt.executeQuery(query);

                if (rset.next()) {
                    int x = JOptionPane.showConfirmDialog(this, "Are you sure to change the '" + CategoryCode + "' " + menuName + " details?", "Update '" + menuName + "'?", JOptionPane.YES_NO_OPTION);
                    if (x == JOptionPane.YES_OPTION) {
                        String UpdateQuery = "UPDATE [SIUnits]\n"
                                + "   SET [UnitName] = '" + CategoryName + "'\n"
                                + "      ,[PurchaseMeasurement] = '" + purchaseMeasurement + "'\n"
                                + "      ,[IssueMeasurement] = '" + issueMeasurement + "'\n"
                                + "      ,[Remarks] = '" + remarks + "'\n"
                                + "      ,[BaseUnitCode] = '" + BaseUnitID + "'\n"
                                + "      ,[UnitType] = '" + Type + "'\n"
                                + " WHERE [UnitCode] = '" + CategoryCode + "'";
                        stmt.execute(UpdateQuery);
                        JOptionPane.showMessageDialog(this, "'" + menuName + "' details are updated.");
                        Refresh();
                    } else if (x == JOptionPane.NO_OPTION) {
                        txtCode.requestFocus();
                    }

                } else if (!rset.next()) {
                    String UpdateQuery = "INSERT INTO [SIUnits]\n"
                            + "           ([UnitCode]\n"
                            + "           ,[UnitName]\n"
                            + "           ,[PurchaseMeasurement]\n"
                            + "           ,[IssueMeasurement]\n"
                            + "           ,[Remarks]\n"
                            + "           ,[BaseUnitCode]\n"
                            + "           ,[UnitType])\n"
                            + "     VALUES\n"
                            + "           ('" + CategoryCode + "'\n"
                            + "           ,'" + CategoryName + "'\n"
                            + "           ,'" + purchaseMeasurement + "'\n"
                            + "           ,'" + issueMeasurement + "'\n"
                            + "           ,'" + remarks + "'\n"
                            + "           ,'" + BaseUnitID + "'\n"
                            + "           ,'" + Type + "')";
                    stmt.execute(UpdateQuery);
                    JOptionPane.showMessageDialog(this, "New '" + menuName + "' is saved.");
                    Refresh();
                }
                rset.close();
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
                JOptionPane.showMessageDialog(this, "Please contact for support.");
            } catch (HeadlessException ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
                JOptionPane.showMessageDialog(this, "Please contact for support.");
            }
        } else if (CategoryCode.isEmpty() || CategoryName.isEmpty() || Type.equals(select)) {
            JOptionPane.showMessageDialog(this, "Please fill all fields before save.", "Empty fields", JOptionPane.OK_OPTION);
            txtCode.requestFocus();
        }
    }

